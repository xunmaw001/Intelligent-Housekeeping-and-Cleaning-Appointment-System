          ],
          fuwujieshao: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='jiazhengzhanghao'){
              this.ruleForm.jiazhengzhanghao = obj[o];
              this.ro.jiazhengzhanghao = true;
              continue;
            }
            if(o=='jiazhengxingming'){
              this.ruleForm.jiazhengxingming = obj[o];
              this.ro.jiazhengxingming = true;
              continue;
            }
            if(o=='lianxidianhua'){
              this.ruleForm.lianxidianhua = obj[o];
              this.ro.lianxidianhua = true;
              continue;
            }
            if(o=='tupian'){
              this.ruleForm.tupian = obj[o].split(",")[0];
              this.ro.tupian = true;
              continue;
            }
            if(o=='xingbie'){
              this.ruleForm.xingbie = obj[o];
              this.ro.xingbie = true;
              continue;
            }
            if(o=='nianling'){
              this.ruleForm.nianling = obj[o];
              this.ro.nianling = true;
              continue;
            }
            if(o=='minzu'){
              this.ruleForm.minzu = obj[o];
              this.ro.minzu = true;
              continue;
            }
            if(o=='zuigaoxueli'){
              this.ruleForm.zuigaoxueli = obj[o];
              this.ro.zuigaoxueli = true;
              continue;
            }
            if(o=='fuwuleixing'){
              this.ruleForm.fuwuleixing = obj[o];
              this.ro.fuwuleixing = true;
              continue;

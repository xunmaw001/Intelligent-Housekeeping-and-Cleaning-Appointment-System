          fuwujine: '',
          fuwujieshao: '',
        },
        fuwuleixingOptions: [],
        rules: {
          jiazhengzhanghao: [
          ],
          jiazhengxingming: [
          ],
          lianxidianhua: [
          ],
          tupian: [
          ],
          xingbie: [
          ],
          nianling: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          minzu: [
          ],
          zuigaoxueli: [
          ],
          fuwuleixing: [
          ],
          fuwumingcheng: [
            { required: true, message: '服务名称不能为空', trigger: 'blur' },
          ],
          fuwuquyu: [
          ],
          gongzuojingyan: [
          ],
          fuwujine: [
            { required: true, message: '服务金额不能为空', trigger: 'blur' },
